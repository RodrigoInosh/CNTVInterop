package cl.subtel.interop.cntv.dto;

public class RepresentanteLegalDTO {
	
		private String nombre;
		private String apellidoPaterno;
		private String apellidoMaterno;
		private String rut;
		private String telefonoFijo;
		private String telefonoOtro;
		private String celular;
		private String email;
		
		public String getNombre() {
			return nombre;
		}
		public void setNombre(String nombre) {
			this.nombre = nombre;
		}
		public String getApellidoPaterno() {
			return apellidoPaterno;
		}
		public void setApellidoPaterno(String apellidoPaterno) {
			this.apellidoPaterno = apellidoPaterno;
		}
		public String getApellidoMaterno() {
			return apellidoMaterno;
		}
		public void setApellidoMaterno(String apellidoMaterno) {
			this.apellidoMaterno = apellidoMaterno;
		}
		public String getRut() {
			return rut;
		}
		public void setRut(String rut) {
			this.rut = rut;
		}
		public String getTelefonoFijo() {
			return telefonoFijo;
		}
		public void setTelefonoFijo(String telefonoFijo) {
			this.telefonoFijo = telefonoFijo;
		}
		public String getTelefonoOtro() {
			return telefonoOtro;
		}
		public void setTelefonoOtro(String telefonoOtro) {
			this.telefonoOtro = telefonoOtro;
		}
		public String getCelular() {
			return celular;
		}
		public void setCelular(String celular) {
			this.celular = celular;
		}
		public String getEmail() {
			return email;
		}
		public void setEmail(String email) {
			this.email = email;
		}

}
